package com.syntax.class07;

public class LoopWhile {

	public static void main(String[] args) {
		// While loop
		
		int time =17;
		 
		if (time<12) {
			System.out.println("Morning");
			System.out.println("___________________________________");
		}
		while (time>12) {
			System.out.println("Morning");
			time--;
		}

	}

}
